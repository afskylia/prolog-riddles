% some of the built-in SWI-Prolog predicates


% sort/2: succeeds if S is the given list sorted and without duplicates
% sort([3,1,4,2,1],S).

% member/2: succeeds if Elem can be unified with one of the members of List
% member(X,[1,2,3,4]).

% append/3: succeeds if List3 unifies with the concatenation of List1 and List2
% append([1,2],[2,3],[1,2,2,3]).